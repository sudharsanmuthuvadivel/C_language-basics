#include <stdio.h>
#include <math.h>
int main()
{
    printf("\nSQRT %0.2f" , sqrt(4));
    printf("\nPOW %0.2f ", pow(2, 3));
    printf("\nABS %d    ", abs(-4));
    printf("\nCEIL %f   ", ceil(3.8));
    printf("\nCEIL %f   ", ceil(3.4));
    printf("\nFLOOR %f  ", floor(3.8));
    printf("\nFloor %f  ",floor(3.4));
    printf("\nROUND %f  ",round(3.8));
    printf("\nROUND %f  ",round(3.4));
    return 0;
}